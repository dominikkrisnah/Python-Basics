# Variabel
# Variabel adalah tempat untuk menyimpan data

# 1. meletakkan / assignment nilai
a = 10
x = 5
panjang = 1000

# 1.1 panggilan pertama
print("Nilai a = ", a)
print("Nilai x = ", x)
print("Nilai panjang = ", panjang)

# 2. memberi penamaan variabel dapat bermacam-macam, contohnya:
nilai_k = 15 # dengan menggunakan underscore
krisna10 = 1000000 # dengan memasukkan unsur angka pada variabel
krisnaH = 20.5 # dengan menggabungkan huruf besar dan kecil

# 1.2 pemanggilan kedua
print("Nilai a = ", a)
a = 15
print("Nilai a = ", a)

# assignment indirect
b = a
print("Nilai b = ", b)