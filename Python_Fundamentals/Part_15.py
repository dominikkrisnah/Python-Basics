# FOR LOOP (Perulangan)

# for kondisi:
#   aksi

# for loop dengan list
angka_krisna = [0, 1, 2, 3, 4, 5]
print(angka_krisna)

for i in angka_krisna:
    print(f"i sekarang -> {i}")
print("keren banget udah bisa FOR LOOP sederhana!!\n")

# for loop dengan range
angka_krisna2 = range(6)
for i in angka_krisna2:
    print(f"i sekarang -> {i}")
print("keren banget udah bisa FOR LOOP sederhana!!\n")
angka_krisna3 = range(2, 6)
for i in angka_krisna3:
    print(f"i sekarang -> {i}")
print("keren banget udah bisa FOR LOOP sederhana!!\n")

# for loop dengan menggunakan string
data_str = "Dominik Ganteng Binggow"
for huruf in data_str:
    print(huruf)
print("keren banget udah bisa FOR LOOP sederhana!!\n")
